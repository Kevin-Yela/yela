def suma(a, b):
    return a + b

def resta(a, b):
    return a - b

# Ejemplo de uso
numero1 = 10
numero2 = 5

resultado_suma = suma(numero1, numero2)
resultado_resta = resta(numero1, numero2)

print("La suma de", numero1, "y", numero2, "es:", resultado_suma)
print("La resta de", numero1, "y", numero2, "es:", resultado_resta)

